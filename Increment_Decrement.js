//INCREMENT

let initial = 200; // Initialize the variable `initial` with a value of 200
console.log("Initial value: ", initial); // Output : 200

initial += 50; // Increment `initial` by 50 and store the new value in the same variable
console.log("Value after Incrementation: ", initial); // Output : 250

//DECREMENT

initial = 200; // Reassign `initial` back to 200
console.log("Initial value: ", initial); // Output : 200

initial -= 50; // Decrement `initial` by 50 and store the new value in the same variable
console.log("Value after Decrementation: ", initial); // Output : 150
