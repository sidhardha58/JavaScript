let a = 75;
let b = 25;

const sum = a + b; //Addition
console.log("The sum is: ", sum); //100

const diff = a - b; //Substraction
console.log("The difference is: ", diff); //50

const mul = a * b; //Multiplication
console.log("The product is: ", mul); //1875

const div = a / b; //Division
console.log("The division is: ", div); //3

const rem = a % b; //Modulos
console.log("The remainder is: ", rem); //0
